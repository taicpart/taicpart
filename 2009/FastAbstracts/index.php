  <h1>Call for Fast Abstracts</h1>

  <p>
    TAIC PART 2008 solicits <b>fast abstracts</b> that explore
    software testing and analysis techniques.  The fast abstract track
    enables the publication of short papers that describe
    <b>late breaking results</b> or <b>works in progress</b>.  This
    TAIC PART track is an ideal venue for PhD students who are
    interested in feedback about dissertation research that is in its
    formative stages.  While fast abstracts are not published in the
    IEEE conference proceedings, the two page papers will be available
    for download from the conference Web Site and authors will present
    their papers alongside the other TAIC PART authors.  Fast abstract
    topics include, but are not limited to, the following areas:
  </p>

  <ul>

    <li> Test Adequacy Criteria
      
    <li> Test Suite Execution

    <li> Test Coverage Monitoring

    <li> Automated Test Data Generation

    <li> Regression Testing

    <li> Automated Debugging and Fault Localization

    <li> Performance Evaluation

    <li> Static and Dynamic Analysis

    <li> Verification and Validation

    <li> Software Reliability Engineering

    <li> Model-Based Testing

    <li> Testing and Formal Methods

    <li> Testing and Model Checking

    <li> Software Testing Process

    <li> Technology Transfer

  </ul>

  <p>
    TAIC PART solicits fast abstracts that focus on the <b>testing and
    analysis</b> of software applications that include, but are not
    limited to, the following types:
  </p>

  <ul>

    <li> Embedded and Real-Time Systems

    <li> Operating Systems 

    <li> Parallel, Distributed, and Cluster Applications

    <li> Service-Oriented Software Systems

    <li> Web Applications

    <li> Event-Based Systems

    <li> File and Storage Systems

    <li> Virtual Machine-Based Applications

    <li> Database Applications

    <li> XML Processing Systems

    <li> Graphical User Interfaces

    <li> Wireless Sensor Networks

    <li> Safety Critical Systems

    <li> AI and Machine Learning Software

    <li> Business Applications

  </ul>

  <h1>Paper Evaluation and Formatting</h1>

      <p>
	All papers will undergo a <b>rigorous examination</b> by at
	least two members of the program committee. The program
	co-chairs and the program committee will provide thoughtful
	and fair reviews that furnish detailed insights into ways for
	improving and extending the papers.  <b>All submitted
	abstracts</b> will be evaluated according to (i) technical
	merit, (ii) relevance to the conference themes and goals,
	(iii) clarity and organization, and (iv) potential for
	generating interesting discussion.  The results described in
	the submitted abstract must be <b>unpublished</b> and
	should <b>not be under review</b> at other workshops,
	conferences, or journals.  To ensure conformity with the
	<a href = "../CallForPapers/">regular papers</a>, the fast
	abstracts should be formatted using the IEEE conference style
	format. Including all references and figures, a fast abstract
	must not exceed the <b>two page</b> limit.
      </p>

  <h1>Fast Abstract Proceedings</h1>

      <p>
	The <b>full text</b> and <b>presentations</b> of the fast
	abstracts will be published in a special section of the TAIC
	PART Web site.  Fast abstracts will be available for
	individual download or as part of a compendium that contains
	all of the abstracts, presentations, and bibliographic
	materials.  Since fast abstracts are not copyrighted, they may
	be subsequently published in conference proceedings or
	journals.  Authors of the accepted fast abstracts are
	encouraged to submit a revised and extended version of their
	paper to <b>TAIC PART 2009</b>.
      </p>

