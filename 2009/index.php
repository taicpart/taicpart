     <h1>Conference Venue</h1>

            <p>
	      The conference will be held at Cumberland Lodge,
              Windsor, UK, September 4 - 6, 2009.
            <a href="http://www.cumberlandlodge.ac.uk/">Cumberland
              lodge</a> is a former royal residence given to the
              nation in 1946 by the late Queen Mother.  It is an ideal
              setting for a productive and enjoyable conference,
              providing world-class conference facilities in an ideal
              location that resonates with centuries of historical
              significance, dating back to the mid 17th century.  Set
              in the heart of Windsor Great Park, yet only 27 miles
              from London and a short distance from Heathrow, it is
              perfectly placed for visitors from home and abroad. Food
              and domestic standards are very high and the atmosphere
              is that of a friendly country house.  As you are
              planning your trip to Windsor, please consult the following
