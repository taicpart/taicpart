     <h1>Special Features</h1>

	      <p>
		TAIC PART features an <b>international program
		committee</b> that will fairly evaluate all of the
		submitted papers and write three or more high quality,
		detailed reviews.  The <b>accepted papers</b> will be
		published with ISBN by the Conference Publishing
		Services (CPS) and they will appear in the IEEE
		digital library. A special issue of the <a href =
		"http://www.elsevier.com/wps/find/journaldescription.cws_home/505732/description#description">Journal
		of Systems and Software</a> will also publish
		revisions of selected papers.  Finally, TAIC PART
		boasts <b>keynote addresses</b>, <b>networking
		opportunities</b>, and <b>social events</b>.
	      </p>		

	      <p>
	Alongside experience reports, research and tool papers, and
	industry and academic challenge articles, TAIC PART 2009 also
	has a <b>fast abstract option</b> that supports the
	publication of short papers describing
	<b>late breaking results</b> or <b>works in progress</b>.
	This TAIC PART track is an ideal venue for PhD students who
	are interested in feedback about dissertation research that is
	in its formative stages.  These two page papers will be
	published in the proceedings and their authors will present
	their papers alongside the other TAIC PART authors.
	</p>

	      <p>
	
		TAIC PART 2009 is the fourth conference in a series of
		highly successful events.  Take a look and discover
		what happened at <a href =
		"http://www2008.taicpart.org">TAIC PART 2008</a>,
		<a href="http://www2007.taicpart.org">TAIC PART
		2007</a>,
		and <a href="http://www2006.taicpart.org">TAIC PART
		2006</a>.  Please consider submitting a paper and/or
		registering to attend the 2009 edition of TAIC PART so
		that you can be part of a premier software testing
		conference.  Individuals with questions about TAIC
		PART are encouraged to contact one of the <a href =
		"Organization/">conference organizers</a>.
	      </p>
	
	    <h1>TAIC PART Rated 'Outstanding'</h1>

            <p>
	      TAIC PART 2006 and 2007 were assessed outstanding and
              internationally leading in all categories by the <a href
              = "http://www.epsrc.ac.uk/default.htm">EPSRC</a>, who
              provided significant funding to hold these events.  The
              criteria included research quality, planning and
              practice, scientific impact, communication of output,
              and benefits to society.  Furthermore, <a href =
              "http://www.epsrc.ac.uk/default.htm">EPSRC</a> also
              funded TAIC PART 2008, thus enabling the conference to
              generate public awareness, communicate research
              findings, and provide practical advice about the
              challenge of software testing.  We invite you to
              <a href = "SubmitPaper/">submit a paper</a> and <a href =
              "Register/">participate</a> in TAIC PART 2009 in order
              to continue the strong tradition of this conference.
	    </p>

