  <h1>Themes and Goals</h1>

  <p>
    The <b>Testing: Academic and Industrial Conference 2009</b> (TAIC
    PART 2009) establishes a forum for the publication of papers
    related to software testing and analysis.  The theme of TAIC PART is
    <b>transferring software testing technology from academia to
    industry</b>.  TAIC PART aims to forge collaborations between
    industry and academia.  The goals of TAIC PART 2009 include the
    articulation of <b>fundamental research questions</b> in the field
    of software testing and analysis.  TAIC PART also focuses on the
    <b>practical challenges</b> that are often faced by software
    developers in industry.
  </p>


  <h1>Call for Papers</h1>

  <p>
    TAIC PART 2009
    solicits <b>experience</b>, <b>tool</b>, <b>original research</b>,
    <b>industry challenge</b>, <b>academic challenge</b>,
    and <b>fast abstract</b> papers that explore software testing and
    analysis techniques.  Research topics include, but are not limited
    to, the following areas:
  </p>

  <ul>

    <li> Test Adequacy Criteria
      
    <li> Test Suite Execution

    <li> Test Coverage Monitoring

    <li> Automated Test Data Generation

    <li> Regression Testing

    <li> Automated Debugging and Fault Localization

    <li> Performance Evaluation

    <li> Static and Dynamic Analysis

    <li> Verification and Validation

    <li> Software Reliability Engineering

    <li> Model-Based Testing

    <li> Testing and Formal Methods

    <li> Testing and Model Checking

    <li> Software Testing Process

    <li> Technology Transfer

  </ul>

  <p>
    TAIC PART solicits papers that focus on the <b>testing and
    analysis</b> of software applications that include, but are not
    limited to, the following types:
  </p>

  <ul>

    <li> Embedded and Real-Time Systems

    <li> Operating Systems 

    <li> Parallel, Distributed, and Cluster Applications

    <li> Service-Oriented Software Systems

    <li> Web Applications

    <li> Event-Based Systems

    <li> File and Storage Systems

    <li> Virtual Machine-Based Applications

    <li> Database Applications

    <li> XML Processing Systems

    <li> Graphical User Interfaces

    <li> Wireless Sensor Networks

    <li> Safety Critical Systems

    <li> AI and Machine Learning Software

    <li> Business Applications

  </ul>

  <h1>Types of Papers</h1>

  <ul>

    <li> <b>Experience</b> reports should provide practical and
      generalizable insights into how to apply and extend existing
      approaches to software testing and analysis. <p>

    <li> <b>Tool</b> papers must focus on the design, implementation,
      and evaluation of software testing and analysis tools. <p>

    <li> <b>Original Research</b> papers will explore novel software
      testing techniques.  These papers should furnish a detailed
      theoretical and/or empirical evaluation of the proposed
      approach. <p>

    <li> <b>Industry Challenge</b> papers must have at least one author
      whose affiliation is non-academic.  These papers will describe a
      real-world software testing problem for which help is sought
      from the academic research community. <p>

    <li> <b>Academic Challenge</b> papers must have at least one author
      whose affiliation is non-industry.  These papers should explain a
      software testing research problem for which help is sought from
      industry partners. <p>

    <li> <b>Fast Abstract</b> papers are short papers that describes
      late breaking results or works in progress.  These papers are
      ideal for PhD students who are interested in receiving feedback
      about dissertation research that is an early stage. <p>

    </ul>

  <h1>Paper Evaluation</h1>

      <p>
	All papers will undergo a <b>rigorous examination</b> by at
	least three members of the program committee. The program
	co-chairs and the program committee will provide thoughtful
	and fair reviews that furnish detailed insights into ways for
	improving and extending the papers.  <b>All submitted
	papers</b> will be evaluated according to (i) relevance to the
	conference themes and goals and (ii) clarity and organization.
      </p>

      <ul>

	<li> <b>Experience reports</b> will be evaluated with respect
	to the significance and generality of the described testing
	experience. <p>

	<li> <b>Research</b> and <b>tool</b> papers will be judged by
	  the technical merit, novelty, and evaluation of the
	  described technique or tool. <p>

	<li> <b>Industry and academic challenge</b> papers will be
	    assessed according to their ability to forge partnerships
	    and ultimately yield successful solutions. <p>

        <li> <b>Fast abstract</b> papers will be evaluated according
	  to their ability to generate discussion and suggest
	  interesting areas for future research.

      </ul>

      <p>
        The results described in the submitted paper must
	be <b>unpublished</b> and should <b>not be under review</b> at
	other workshops, conferences, or journals.
      </p>

  <h1>Paper Formatting</h1>

      <p>
	Papers should be formatted using the IEEE conference style
	format. Including all references and figures,
	a <b>research</b> paper must not exceed the <b>ten page</b>
	limit and the <b>experience</b>, <b>tool</b>, and <b>industry
	and academic challenge</b> papers should be not longer
	than <b>five pages</b>.  Finally, <b>fast abstract</b> papers
	must be two pages in length.  Authors should submit a PDF
	version of their paper through the <a href =
	"../SubmitPaper/index.php">Submit Paper</a> Web site.
      </p>

  <h1>Conference Proceedings</h1>

      <p>
	The <b>accepted papers</b> will be published with ISBN by
	Conference Publishing Services (CPS) and they will appear in
	the IEEE digital library.  A special issue of the <a href =
	"http://www.elsevier.com/wps/find/journaldescription.cws_home/505732/description#description">Journal
	of Systems and Software</a> will also publish revisions of
	selected papers from TAIC PART 2009.
      </p>

