	    <h1>TAIC PART Rated 'Outstanding'</h1>

            <p>
	      TAIC PART 2006 and 2007 were assessed outstanding and
              internationally leading in all categories by the <a href
              = "http://www.epsrc.ac.uk/default.htm">EPSRC</a>, who
              provided significant funding to hold these events.  The
              criteria included research quality, planning and
              practice, scientific impact, communication of output,
              and benefits to society.  Furthermore, <a href =
              "http://www.epsrc.ac.uk/default.htm">EPSRC</a> also
              funded TAIC PART 2008, thus enabling the conference to
              generate public awareness, communicate research
              findings, and provide practical advice about the
              challenge of software testing.  We invite you to
              <a href = "Register/">participate</a> in TAIC PART 2008 in order
              to continue the strong tradition of this conference.
	    </p>

