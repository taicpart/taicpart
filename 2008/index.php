<div style="margin-top:-.1in" align="center">

	      <p>
		Among computer science and software engineering
		activities, <b>software testing</b> is a perfect
		candidate for the union of academic and industrial
		minds.  TAIC PART is a unique event that strives to
		combine the important aspects of a software
		testing <b>conference</b>, <b>workshop</b>, and
		<b>retreat</b>.
	      </p>

	      <p>
	      TAIC PART brings together
	      <b>industrialists</b> and <b>academics</b> in an
	      environment that promotes meaningful collaboration on
	      the challenges of software testing.  TAIC PART is
	      sponsored by representatives from both industry and
	      academia. The conference brings together software
	      developers, end users, and academic researchers who work
	      on both the <b>theory</b> and <b>practice</b> of
	      software testing.
	      </p>

	      <p>
		Interested in learning more about the TAIC PART
		conference series?  Please visit the <a href =
		"http://www2009.taicpart.org">TAIC PART 2009
		conference Web site</a>.

     <h1>Special Features</h1>

	      <p>
		TAIC PART features an <b>international program
		committee</b> that will fairly evaluate all of the
		submitted papers and write three or more high quality,
		detailed reviews.  The <b>accepted papers</b> will be
		published with ISBN by the IEEE Computer Society and
		they will appear in the IEEE digital library. A
		special section of <a href =
		"http://www.elsevier.com/wps/find/journaldescription.cws_home/525444/description#description">Information
		and Software Technology</a> will also publish
		revisions of selected papers.  Finally, TAIC PART
		boasts
		<b>keynote addresses</b>, <b>networking
		opportunities</b>, and <b>social events</b>.
	      </p>		

